def _extract_code_block(text: str) -> str:
    """Extract content from first ```...``` block."""
    import re
    m = re.search(r"```(?:typescript|ts|javascript|js|python)?\n([\s\S]*?)```", text)
    if m:
        return m.group(1).strip()
    return ""
