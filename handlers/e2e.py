@router.message(Command("e2etest"))
async def cmd_e2etest(msg: Message) -> None:
    """AI-guided Playwright E2E test against a URL."""
    if not is_allowed(msg):
        return

    url = (msg.text or "").removeprefix("/e2etest").strip()
    if not url:
        await msg.answer(
            "Usage: <code>/e2etest https://your-site.com</code>\n"
            "I'll generate and run Playwright tests against that URL.",
            parse_mode="HTML",
        )
        return

    if not url.startswith(("http://", "https://")):
        url = "https://" + url

    status_msg = await msg.answer(
        f"\U0001f9ea Starting E2E test for <code>{html_mod.escape(url)}</code>\u2026\n"
        "Step 1/4: Generating test plan…",
        parse_mode="HTML",
    )

    try:
        from llm_client import chat

        # Step 1: Generate test plan
        plan_prompt = textwrap.dedent(f"""\
            You are a senior QA engineer. Analyse the website at: {url}

            Generate a Playwright E2E test plan. Include:
            1. List of 3-5 critical user journeys to test
            2. Key pages and interactions to verify
            3. Things to check: auth, forms, navigation, responsive layout, API calls

            Then write a complete working Playwright test file (TypeScript) that:
            - Tests all the journeys you listed
            - Uses data-testid selectors where possible, fallback to ARIA roles
            - Includes proper async/await and expect assertions
            - Saves screenshots on failure
            - Can run headlessly with: npx playwright test

            Output ONLY the TypeScript test file content inside a ```typescript code block.
            No explanation before or after the code block.
        """)

        plan_response, plan_model = await chat(plan_prompt, agent_key="reviewer")

        # Extract code block
        test_code = _extract_code_block(plan_response)
        if not test_code:
            test_code = plan_response  # fallback: use raw response

        await status_msg.edit_text(
            f"\U0001f9ea E2E test for <code>{html_mod.escape(url)}</code>\n"
            f"Step 2/4: Writing test file\u2026 (via {html_mod.escape(plan_model)})",
            parse_mode="HTML",
        )

        # Step 2: Write test file
        _E2E_WORK_DIR.mkdir(parents=True, exist_ok=True)
        test_file = _E2E_WORK_DIR / "generated.spec.ts"
        config_file = _E2E_WORK_DIR / "playwright.config.ts"

        test_file.write_text(
            f"// Generated by Legion for: {url}\n"
            f"// Model: {plan_model}\n\n"
            + test_code,
            encoding="utf-8",
        )

        config_file.write_text(
            textwrap.dedent(f"""\
                import {{ defineConfig }} from '@playwright/test';
                export default defineConfig({{
                  testDir: '.',
                  use: {{
                    baseURL: '{url}',
                    headless: true,
                    screenshot: 'only-on-failure',
                    video: 'off',
                  }},
                  reporter: [['json', {{ outputFile: '/tmp/legion_e2e/results.json' }}], ['line']],
                }});
            """),
            encoding="utf-8",
        )

        await status_msg.edit_text(
            f"\U0001f9ea E2E test for <code>{html_mod.escape(url)}</code>\n"
            "Step 3/4: Installing Playwright + running tests\u2026",
            parse_mode="HTML",
        )

        # Step 3: Run tests via computer_agent shell
        try:
            from llm_client import run_shell_command
            # Check/install playwright
            check = await run_shell_command(
                "which npx && npx playwright --version 2>&1 || echo 'NOT_INSTALLED'"
            )
            if "NOT_INSTALLED" in check or "not found" in check:
                install_out = await run_shell_command(
                    "npm install -g playwright @playwright/test 2>&1 | tail -5"
                )
                await status_msg.edit_text(
                    f"\U0001f9ea E2E test — installed Playwright:\n<pre>{html_mod.escape(install_out[:400])}</pre>\n"
                    "Running tests\u2026",
                    parse_mode="HTML",
                )
                await run_shell_command(
                    "npx playwright install --with-deps chromium 2>&1 | tail -3"
                )

            run_out = await run_shell_command(
                f"cd {_E2E_WORK_DIR} && npx playwright test generated.spec.ts "
                f"--config playwright.config.ts 2>&1 | tail -40",
                timeout=120,
            )

        except Exception as shell_err:
            run_out = f"Shell execution failed: {shell_err}\n(Is computer_agent available?)"

        # Step 4: Parse results and summarise
        await status_msg.edit_text(
            f"\U0001f9ea E2E test for <code>{html_mod.escape(url)}</code>\n"
            "Step 4/4: Analysing results\u2026",
            parse_mode="HTML",
        )

        summary_prompt = textwrap.dedent(f"""\
            Playwright test run output:
            ```
            {run_out[:3000]}
            ```

            Summarise in this format:
            ## E2E Report — {url}
            ✅ PASSED: N
            ❌ FAILED: N
            ⏭ SKIPPED: N

            ### Failed Tests
            (list each failure with error)

            ### Verdict
            (PASS / FAIL / PARTIAL + one-line recommendation)
        """)

        summary, _ = await chat(summary_prompt, agent_key="analyst")

        # Send test code + summary
        code_snippet = test_code[:1500] + ("\n...(truncated)" if len(test_code) > 1500 else "")
        final = (
            f"<b>\U0001f9ea E2E Test Complete — {html_mod.escape(url)}</b>\n\n"
            f"{html_mod.escape(summary)}\n\n"
            f"<b>Generated Test File:</b>\n"
            f"<pre>{html_mod.escape(code_snippet)}</pre>"
        )
        await status_msg.delete()
        await send_chunked(msg, final)

    except Exception as e:
        await status_msg.edit_text(
            f"\u274c E2E test failed: <code>{html_mod.escape(str(e)[:300])}</code>",
            parse_mode="HTML",
        )


